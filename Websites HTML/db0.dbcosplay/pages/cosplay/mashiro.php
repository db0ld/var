<h1>Mashiro</h1>
	
<h2>Qui est ce personnage ?</h2>
<p>Personnage principal de la série "Tayutama - Kiss on my deity -". C'est une sorte de dieu renard, d'où sa queue et ses orreilles. Elle est gentille mais un peu naïve.</p>
<h2>Pourquoi avoir fait ce costume ?</h2>
<p>Tayutama est une série que je fansubbe et j'aime beaucoup faire les costumes des séries que je fansubbe (Kodomo no Jikan aussi). Je voulais aussi m'attaquer à un costume un peu plus compliqué que ce que j'ai l'habitude de faire.</p>
<h2>Avis perso sur ce costume.</h2>
<p>Les ourlets sont pas toujours très joli, mais dans l'ensemble, c'est réussi, selon moi.</p>
<h2>Groupe/Solo ?</h2>
<p>Je compte porter ce costume en groupe "Uniforme scolaire" à Paris Manga le 13 septembre 2009. Nous serons à peu près cinq filles en uniforme.</p>

