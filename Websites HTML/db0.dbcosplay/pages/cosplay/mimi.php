<h2>Qui est ce personnage ?</h2>
<p>De la série "Kodomo no Jikan". Une série ecchi-pedobear à la fois drôle et attachante.</p>
<h2>Pourquoi avoir fait ce costume ?</h2>
<p>Ma série préférée !! Voulais monter un groupe avec ma femme dedans (Rin).</p>
