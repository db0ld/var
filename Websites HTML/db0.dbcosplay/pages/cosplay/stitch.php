   <h1>Stitch live - Place à la dance !</h1>
	
<h2>Qui est ce personnage ?</h2>
   <p>Un des spectacles de Disneyland Paris presente Stitch DJ et sa bande de danseurs. Je compte incarner l'une des danceuses du groupe avec Stitch et d'autres danseurs.</p>
<h2>Pourquoi avoir fait ce costume ?</h2>
   <p>J'ai adoré ce spectacle à Disneyland et j'y suis beaucoup retourne pour le voir. J'aimais beaucoup le costume des danseuses, futuriste.</p>
<h2>Avis perso sur ce costume.</h2>
   <p>Je ne pense pas qu'il sera difficile.</p>
<h2>Groupe/Solo ?</h2>
<p>Un groupe avec un Stitch en mascotte et sans doute d'autres danseurs.</p>
<h2>Composants du costume.</h2>
   <p>Robe; epaulettes; Bottes; Perruque; Chapeau; Maquillage.</p>

