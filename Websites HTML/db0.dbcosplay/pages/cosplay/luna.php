<h1>Luna Lovegood</h1>
	
<h2>Qui est ce personnage ?</h2>
<p>Personnage secondaire de la célébre série "Harry Potter". C'est une fille à part, elle vit dans son monde et voit des choses que les autres ne voient pas. On l'appelle "Loufoka Lovegood" parce qu'elle est étrange.</p>
<h2>Pourquoi avoir fait ce costume ?</h2>
<p>Je trouve que je lui ressemble, que je suis toujours dans la lune. C'est dans la série le personnage qui me correspond le mieux. De plus, en ayant fait un test pour savoir dans quelle maison je serais à Poudlard, j'ai été assignée à Serdaigle, qui correspond à la maison de Luna. Lorsqu'un groupe "Harry Potter" a commencé à se former sur Cosplayforum, je n'ai pas hésité à y participer en choisissant ce personnage !</p>
<h2>Avis perso sur ce costume.</h2>
<p>Je n'ai pas commencé à le réaliser, mais je sais déjà comment je vais le faire.</p>
<h2>Groupe/Solo ?</h2>
<p>Je compte le porter lors de Japan Expo 2010 avec un groupe Harry Potter.</p>

