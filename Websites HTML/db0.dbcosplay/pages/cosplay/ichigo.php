   <h1>Ichigo-chan</h1>
	
<h2>Qui est ce personnage ?</h2>
   <p>Inspiré des petites fraises de l'ending d'Ichigo 100%, notre duo de fraise fait du para para.</p>
<h2>Pourquoi avoir fait ce costume ?</h2>
   <p>Une idee de Yumi, une tres proche amie cosplayeuse. Je crois qu'elle a eu cette idee quand elle a vu que je conaissais la chore de Ike Ike (ending de Ichigo 100%).</p>
<h2>Avis perso sur ce costume.</h2>
   <p>Nous avons deja une idee generale du costume et on compte le faire a deux. J'espere qu'il sera tres mignon.</p>
<h2>Groupe/Solo ?</h2>
<p>En duo avec Yumi, nous serons deux petites fraises qui font du para para.</p>
<h2>Composants du costume.</h2>
   <p>Robe; sous-robe; bracelet; bottes; perruque; chapeau.</p>

