<h1>Kusakabe Sakura-kun</h1>
	
<h2>Qui est ce personnage ?</h2>
<p>Souffre-douleur de la série "Bokusatsu Tenshi Dokuro-chan". Une série en 4 épisodes très drôle et complétement déjanté où Sakura se fait constamment tuer puis rescuciter.</p>
<h2>Pourquoi avoir fait ce costume ?</h2>
<p>J'ai beaucoup aimé la série et je voulais faire un groupe.</p>
<h2>Avis perso sur ce costume.</h2>
<p>M'a donné du fil à retordre ! Jusqu'à aujourd'hui, le costume qui m'a pris le plus de temps : 3 vestes à faire, 2 jupes, des anneaux d'anges, la massue/batte Excaliborg, l'arme de Sabato, les cornes de Sabato. Le résultat est cependant à la hauteur du travail fourni. J'ai eu beaucoup de fans de la massue/batte Excaliburg ^^</p>
<h2>Groupe/Solo ?</h2>
<p>Toujours en groupe de 3, avec Sakura-kun et Sabato-chan. Remarque : Je suis toujours Dokuro-chan, mais les personnes qui portent les costumes de Sabato et Sakura ne sont jamais les mêmes. Si vous êtes interessés pour porter un de ces deux costumes à mes côtés, n'hésitez pas ! Porté à Chibi Japan Expo Sud 2009 sur scène et à Mang'azur 2009 en libre. Sera porté lors du concert Pen of Chaos à Japan Expo 2009.</p>

