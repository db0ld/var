<h1>Danceuse Orientale</h1>
	
<h2>Qui est ce personnage ?</h2>
<p>Une danceuse du ventre orientale.</p>
<h2>Pourquoi avoir fait ce costume ?</h2>
<p>Pendant mon voyage en Tunisie, j'ai vue beaucoup de danceuses du ventre lors de soirées à théme. J'ai beaucoup aimé leurs tenues. Pendant un voyage en Turquie, j'ai décidé de faire un costume dans ce style et j'ai acheté les tissus et "bling bling" là-bas, puis j'ai réalisé le costume en France.</p>
<h2>Avis perso sur ce costume.</h2>
<p>J'ai été aidée par ma grand-mère couturière pour réaliser ce costume, il m'a pris des mois à réaliser et je suis très contente du résultat.</p>
<h2>Groupe/Solo ?</h2>
<p>Jamais porté en convention, et je pense ne jamais le porter. Il est assez dénudé et carrément pas dans l'esprit convention jap'anime.</p>
<h2>Composants du costume.</h2>
<p>Haut + Bas + Perruque</p>

