<h1>Sailor Moon</h1>
	
<h2>Qui est ce personnage ?</h2>
<p>Sailor Moon, quoi ! J'en sais pas plus que ça, j'ai jamais regardé la série.</p>
<h2>Pourquoi avoir fait ce costume ?</h2>
<p>J'adore les gros noeuds ! Et elle en a deux, en plus ! Un devant, un derrière. Anyaa~~ À moi les gros noeuds !</p>
<h2>Avis perso sur ce costume.</h2>
<p>Il m'a donné du fil à retordre et ne me satisfait toujours pas... Au départ, beaucoup trop gros (je me vois toujours trop grosse) donc ma grand-mère m'a aidé à reprendre ma bêtise et au niveau de la taille, ça va. Par contre, la jupe est très longue. En fait, au final, ça donne un mélange de sa version uniforme et sa version transformée.</p>
<h2>Groupe/Solo ?</h2>
<p>Porté une seule fois en convention lors de la Japan Expo 2008. Essaie deséspérement de trouver un garçon qui veuille bien la porter. Oui, c'est un de mes fantasmes, un mec en sailor moon. Et puis, si un groupe de Sailor veut bien de moi, je prends ! Je participe !</p>
<h2>Composants du costume.</h2>
<p>Robe + Rajouts couettes + Collier + Gants(x2) + Bottes + Fausse frange</p>

