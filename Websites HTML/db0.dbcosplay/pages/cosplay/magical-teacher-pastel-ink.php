   <h1>Magical Teacher Pastel Ink</h1>
	
<h2>Qui est ce personnage ?</h2>
   <p>C'est la version transformee du personnage principal de la serie Moetan. Cette petite fille toute mignonne, malgre les apparences, n'est pas en primaire mais au lycee. Elle fait la rencontre d'un canard qui parle et qui va lui donner des pouvoirs magiques.</p>
<h2>Pourquoi avoir fait ce costume ?</h2>
   <p>Une serie que j'adore, un personnage que j'adore, une tenue que j'adore.</p>
<h2>Avis perso sur ce costume.</h2>
   <p>Je le trouve pas trop mal. J'aime particulierement les chaussures et la baguette magique. Le style "maillot de bain scolaire" me plait bien.</p>
<h2>Groupe/Solo ?</h2>
<p>Je l'ai porte en groupe a Japan Expo 2009 avec Nijihara Ink (costume prete a une amie) et a Harajuku 2009 avec Nijihara Ink (prete a Otaku love) et Dandy version bondage. J'aimerai beaucoup faire un groupe avec toutes les membres du groupe des super heroines.</p>
<h2>Composants du costume.</h2>
   <p>Perruque; Casquette; haut marinier; maillot de bain scolaire; manches; rubans de manches; baguette magique; chaussettes; chaussures; Canard.</p>

