<h1>Amane Misa Misa</h1>
	
<h2>Qui est ce personnage ?</h2>
<p>L'un des personnages principaux de la série Death note. Gros succés que l'on ne présente plus.</p>
<h2>Pourquoi avoir fait ce costume ?</h2>
<p>J'ai commencé à lire Death Note, et du coup j'ai fait ce "cosplay".</p>
<h2>Avis perso sur ce costume.</h2>
<p>"Cosplay" entre guillemets : LE cosplay placard par exellence. Mis à part le ruban, tout à été acheté : Chaussettes, jupe de gothique lolita, petit haut. J'ai fait un peu de couture pour le haut qui m'allait trop grand.</p>
<h2>Groupe/Solo ?</h2>
<p>Porté une fois en convention lors de la Japan Expo 2008, avec 2 Light/Raito. Ouverte à toute proposition de groupe !</p>
<h2>Composants du costume.</h2>
<p>Haut + Jupe + Chaussettes + Death Note + Cravate</p>

