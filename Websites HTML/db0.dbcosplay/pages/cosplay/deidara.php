<h1>Deidara</h1>
	
<h2>Qui est ce personnage ?</h2>
<p>Un méchant vilain pas beau de l'Akatsuki, dans Naruto. Le seul qui sourit. Il a des langues dans les mains et sur le ventre.</p>
<h2>Pourquoi avoir fait ce costume ?</h2>
<p>Je trouvais que la cape de l'Akatsuki avait beaucoup de style, donc je l'ai faite. J'ai choisi que ce serait celle de Deidara car comme je suis blonde, pas besoin de perruque.</p>
<h2>Avis perso sur ce costume.</h2>
<p>Très réussi, je trouve. Et très pratique quand il fait froid ^^</p>
<h2>Groupe/Solo ?</h2>
<p>Je ne l'ai jamais porté en convention, mais je l'ai souvent prêté à des personnes voulant tenter de porter un costume "pour voir ce que ça fait". Je suis ouverte à toute proposition de groupe !</p>
<h2>Composants du costume.</h2>
<p>Manteau + Chaussues + Bandeau + Sous-chaussures + Pantalon + T-shirt + Sacoche + Mitaines(x2) + Truc blanc sur la jambe + Bague</p>

