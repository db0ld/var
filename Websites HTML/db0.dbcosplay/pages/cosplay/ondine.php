<h1>Ondine / Misty / Kasumi</h1>
	
<h2>Qui est ce personnage ?</h2>
<p>L'amie de Sacha/Ash dans Pokémon première saison. Elle le suit car il doit lui rembourser son vélo.</p>
<h2>Pourquoi avoir fait ce costume ?</h2>
<p>Fan de Pokémon depuis l'enfance et pour longtemps encore ! Je voulais faire un groupe Pokémon dans lequel j'avais prévu de faire Jessie. Puis j'ai trouvé un Sacha donc j'ai fait Ondine, mais je n'ai pas abandonnée Jessie.</p>
<h2>Avis perso sur ce costume.</h2>
<p>J'ai honte, mais c'est encore un cosplay placard ^^ J'ai acheté un short qui ne m'allait pas et un t-shirt jaune et des bretelles rouges. Après un régime, le short me va !</p>
<h2>Groupe/Solo ?</h2>
<p>Jamais porté. Si je le porte, ce sera en groupe ! Interessé ? N'hésitez pas !</p>

