<h1>Kagami Kuro</h1>
	
<h2>Qui est ce personnage ?</h2>
<p>De la série "Kodomo no Jikan". Une série ecchi-pedobear à la fois drôle et attachante.</p>
<h2>Pourquoi avoir fait ce costume ?</h2>
<p>Ma série préférée !! Voulais monter un groupe avec ma femme dedans (Rin).</p>
<h2>Avis perso sur ce costume.</h2>
<p>Je trouve la robe plutot bien reussie. Elle a l'effet large que je voulais et j'ai bien reussi les volants en bas.</p>
<h2>Groupe/Solo ?</h2>
<p>Uniquement en groupe avec Rin et Mimi. Sera sur scène Jeudi de la Japan Expo 2009.</p>

