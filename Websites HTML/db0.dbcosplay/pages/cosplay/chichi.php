<h1>Chichi Adulte</h1>
	
<h2>Qui est ce personnage ?</h2>
<p>Chichi est un personnage de la série Dragon Ball Z. Cette version est sa version adulte, c'est-à-dire, quand elle est marié avec Sangoku et qu'elle a son fils San Gohan. Elle a alors arrêté de se battre et se préoccupe de l'éducation de son fils.</p>
<h2>Pourquoi avoir fait ce costume ?</h2>
<p>C'est mon tout premier costume ! Quand je l'ai fait, je ne savais même pas que le cosplay était une pratique connue^^ Je n'allais pas en convention et je ne connaissais aucun autre manga que DBZ. Pourquoi l'ai-je fait ? Depuis toute petite, j'adore me "déguiser" donc j'ai voulu m'habiller comme mon personnage préféré. Tout simplement.</p>
<h2>Avis perso sur ce costume.</h2>
<p>Comme c'est mon tout premier costume, j'étais nulle en couture ^^ Du coup, il est pas très joli et le tissus ne va pas trop bien. Mais bon, c'est ça, la nostalgie des premiers costumes...</p>
<h2>Groupe/Solo ?</h2>
<p>Je n'ai porté qu'une fois ce costume. C'était lors de la Japan Expo 2008. Je suis montée pour la première fois sur scéne avec un groupe DBZ. Un groupe que j'apprécie beaucoup avec qui j'ai gardé contact ! Aujourd'hui, je souhaite regrouper les cosplayeurs DBZ via le site http://dbcosplay.fr ! Donc je suis ouverte à tout groupe et ne porterai jamais ce costume seule.</p>

