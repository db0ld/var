<h3>Lieu de vie</h3>
<div class="p">Née à Nice, j'ai vécu jusqu'à l'âge de 4 ans en plein centre ville. Lors de la naissance de mon petit frère, l'appartement devenant trop petit, ma famille et moi-même avons démenagé en bordure de la ville, sur la colline de l'abadie, appartenant au village de St André de la Roche. À mes 18 ans, pour mes études, je loue un appartement en bordure de Paris, au Kremlin-Bicêtre, dans le Val-de-Marne (94).</div>

