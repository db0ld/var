		<div class="title">Détails</div>
		
<b>Titre Original</b> Kodomo no Jikan<br>
<b>Titre français</b> Le temps de l'enfance<br>
<b>Site officiel</b> <a href="http://www.kojika-anime.com/" target="_blank">Kojika-anime.com</a><br>
<b>Année de production</b> 2007<br>
<b>Studio</b> <a href="http://www.stbarcelona.com/" target="_blank">Studio Barcelona</a><br>
<b>Genre</b> Comédie - Ecchi<br>
<b>Auteur</b> Watashiya Kaworu
	
		<div class="title">Synopsis</div>
		
	Aoki Daisuke sensei est un tout nouveau professeur en primaire et débute sa carrière dans une classe d’enfants à priori calme, mais il découvrira que même à 9 ans on peut être bien précoce. Il fera la rencontre de Rin Kokonoe, Kuro Kagami et Mimi Usa, trois jeunes filles de sa classe qui sont prêtes à lui apporter bien des soucis, surtout pour la petite Kokonoe. Cette dernière fera tout pour que son sensei chéri tombe amoureux d’elle, par de nombreux pièges parfois osés, et rendra sa vie de nouveau professeur très difficile. Au fil du temps, Aoki sensei découvrira que tout n’est pas toujours rose dans la vie de ses élèves et fera de son mieux pour les aider à surmonter les problèmes, même s’il met parfois en jeu son poste d’éducateur. Reste à voir dans quelles galères il se mettra, surtout avec le trio Kokonoe, Kagami et Usa.
		
