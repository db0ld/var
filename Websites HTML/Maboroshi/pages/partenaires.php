<div class="titre">Devenir partenaires</div>
<p>Pour devenir partenaire avec le site officiel de Maboroshi no fansub et ainsi voir sa bannière affichée sur toutes les pages, il faut remplir plusieurs conditions :</p>
<ul>
<li><b>Être une autre team de fansub.</b> Dans ce cas, il ne faut pas que vous distribuiiez des animes licenciés et que nous subbions les mêmes animes. Nous serons alors plus ouverts pour d'éventuelles coproductions. En cas de soucis de votre côté comme du nôtre, des membres des deux équipes pourront s'entraider. Notre bannière doit être affichée sur votre site aussi en évidence que sur le notre.</li>
<li><b>Être un site distribuant nos animes en ddl, streaming ou autre.</b> Dans ce cas, reportez-vous <a href="?page=distribuant">à ce lien</a>.</li>
<li><b>Être un site de réferencement.</b> Dans ce cas, reportez-vous <a href="?page=distribuant">à ce lien</a>.</li>
</ul>
<p>Si et seulement si les conditions sont remplies, vous pouvez nous proposer un partennariat en remplissant et en nous envoyant le formulaire suivant :</p>
