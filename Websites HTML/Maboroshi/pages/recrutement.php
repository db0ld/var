<div class="titre">Recrutement</div>
<p>Tu souhaites rejoindre notre équipe et nous aider dans la réalisation des fansubs de nos séries ? N'hésite pas à postuler !<br />
Cependant, avant ça, tu dois savoir qu'en envoyant une candidature, tu t'engages à :</p>
<ul>
<li>Te tenir aux engagements que tu auras précisé dans ta candidature</li>
<li>Ne pas mentir ou gonfler tes exploits dans ta candidature</li>
<li>Rejoindre nos rangs si ta candidature est acceptée</li>
<li>Ne pas insulter, provoquer, menacer, s'énerver si celle-ci est refusée</li>
<li>Être disponible pour l'équipe à tout moment ou préciser à quels moments tu seras indisponible</li>
<li>Evitez les retards au niveau des rendus</li>
<li>Ne pas bâcler le travail réalisé sur une realease</li>
<li>Respecter le directeur de l'équipe, ne pas contredire ses décisions</li>
<li>Ne pas demander de rémunération : le fansub est une activité enrichissante mais bénévole</li>
</ul>
<p>Si te sens pouvoir tenir ces engagements, alors nous attendons ta candidature. Nous rappelons aussi que nous sommes une équipe très gentille et à l'écoute de tous, donc il ne faut pas hésiter à nous poser des questions.
</p>
