<h2>Editeur</h2>
<p><b>En quoi consiste ce rôle ?</b><br />
Faire de l'épisode quelque chose de joli en choisissant polices et couleurs mais surtout couvrir panneaux, affiche et autre texte en japonais dans la vidéo de façon à ce que l'on est l'impression que l'épisode était écrit en français au départ.</p>
<p><b>Quelles compétances sont nécéssaires ?</b><br />
De l'expérience dans ce domaine, c'est-à-dire soit avec After Effect, soit via le language ass à connaître.</p>
<p><b>Quel matériel est requis ?</b><br />
Logiciels : Aegisub ou After Effect.<br />
Fichiers : Raw(vidéo sans sous-titre) à télécharger, Script ass traduit, Photoshop(facultatif).</p>
