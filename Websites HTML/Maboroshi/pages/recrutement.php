<h2>Adapt</h2>
<p><b>En quoi consiste ce rôle ?</b><br />
Passer derrière le traducteur pour modifier s'il le faut les phrases en fonction des interlocuteurs. Exemple : Une fille de 6 ans qui dit "Je ne pense pas avoir les compétances nécéssaire pour effectuer ce travail", ce n'est pas normal et ce doit être remplacé par "Je ne peux pas faire ce travail".</p>
<p><b>Quelles compétances sont nécéssaires ?</b><br />
Savoir écrire exellement bien français, savoir se mettre dans la peau d'un personnage et construire des phrases agréables.</p>
<p><b>Quel matériel est requis ?</b><br />
Logiciels : Bloc-note, Aegisub, Media Player Classic.<br />
