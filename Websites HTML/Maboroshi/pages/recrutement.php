<div class="titre">Les rôles</div>
<p>Vouloir nous rejoindre, pas de problème, mais il faut savoir ce que tu sais/veux faire ! Ci dessous la liste des activités effectuées au sein de l'équipe, accompagnée d'une description, d'un test à renvoyer dans la candidature et du nombre de postes à promouvoir.</p>
<p><i>Pour tout les rôles, un ordinateur fonctionnel, une connexion internet potable et des enceintes (ou écouteurs) sont requis.</i></p>

<h2>Traducteur</h2>
<p><b>En quoi consiste ce rôle ?</b><br />
Traduire l'épisode de l'anglais au français ou du japonais au français.</p>
<p><b>Quelles compétances sont nécéssaires ?</b><br />
Parler et comprendre correctement l'anglais et le français. Savoir écrire en français convenablement.</p>
<p><b>Quel matériel est requis ?</b><br />
Logiciels : Bloc-note, Aegisub, Media Player Classic.<br />
Fichiers : Vidéos en vosta à télécharger, Script ass (si disponible).</p>
