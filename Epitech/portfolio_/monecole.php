L'école Epitech a été créée en 1999 dans la mouvance de l'EPITA pour accueillir les bacheliers désirant apprendre l'informatique et ne possédant pas nécessairement le baccalauréat scientifique. L'école fait partie du IONIS Institute Of Technology.<br /><br />

Cette école présente la particularité de former ses étudiants en 5 ans par la pratique, ce qui est atypique parmi les écoles d'informatique. Elle recrute des étudiants titulaires d'un baccalauréat, essentiellement dans les sections scientifiques (S ou STI) tout en permettant une sélection d'étudiants d'autres sections (ES, L, STG, etc.). L'école recrute également en parallèle des étudiants issus d'une formation de niveau bac+2 et bac+3. En janvier 2008 le diplôme délivré de l'Epitech à l'issue du cursus de cinq ans d'« expert en technologies de l'information » a été enregistré au sein du RNCP[1] et du CNCP en tant que « niveau I ». L'école ne délivre pas un diplôme d'ingénieur car elle n'est pas habilitée par la Commission des titres d'ingénieurs (CTI) (organisme officiel chargé des formations d'ingénieurs en France).<br /><br />

L'école délivre également en fin de troisième année un premier titre de diplôme à ses étudiants qui est le Bachelor of Computer Science.<br /><br />

Depuis janvier 2007, l’EPITECH a ouvert une école à Dakar au Sénégal. De nouvelles sections ont ouvert à l'automne 2007 à Casablanca au Maroc et Dalian en Chine. À la rentrée 2007, l’EPITECH a rejoint le campus urbain des ISEG[2] et se développe en France à Bordeaux, Lille, Lyon, Nantes, Strasbourg et Toulouse. Depuis la rentrée 2008, l'école est également présente à Nice, Montpellier et Nancy et à la rentrée 2009 elle ouvrira à Marseille et Rennes.<br /><br />

