<b>Bonjour et bienvenue, chers visiteurs !</b><br /><br />

Je m'appelle Zakariya Kevin, né le 12/03/1989 à Boulogne-Billancourt.<br />
Je suis actuellement élève en 2e année à l'école d'expertise informatique Epitech menant en cinq années à un diplôme d'Expert en Technologie de l'Information de niveau 1.<br /><br />

Je suis un passionné d'informatique depuis ma plus tendre enfance puisque j'ai toujours été entouré, grâce à mes parents, par les dernières technologies, ce qui m'a permis d'apprendre précocement à "toucher à tout" en ce qui concerne l'informatique. J'ai toujours été un modèle pour mon entourage à ce niveau-là, et celui-ci sait qu'il peut toujours compter sur moi pour d'éventuelles aides dans le domaine informatique.<br />
J'ai rapidement acquis les techniques de bases de l'informatique, mais j'ai été au départ attiré plutôt vers tout ce qui concerne le web puisque j'étais dès mon plus jeune âge administrateur de mes propres sites sur mes serveurs dédiés.<br />
Le choix d'orientation après mon bac S me fut alors évident : il fallait que je fasse carrière dans l'informatique. J'ai alors choisi l'école Epitech puisqu'elle correspondait exactement à la vision que j'avais de l'approfondissement de mon apprentissage dans l'informatique, avec une pédagogie autonome me correspondant tout à fait.<br />
Aujourd'hui, comme dit précédemment, j'arrive à la fin de ma deuxième année dans cette école. Celle-ci m'a permis d'apprendre à travailler au sein d'un groupe, à savoir optimiser mon emploi du temps, à être capable de prendre les bonnes décisions et à travailler au sein d'une entreprise.<br />
Je ne m'étalerai pas sur ce que j'ai fait concrètement durant ces deux années puisque se trouve à la page "expérience" le détail complet des projets que j'ai réalisés au sein de l'école.<br />
N'hésitez donc pas à vous y intéresser et si besoin à me contacter.<br />
Tous ces projets sont le fruit de mon apprentissage et le reflet de mes capacités.<br /><br />

Je vous souhaite donc une très bonne visite sur ce portfolio et espère que vous y trouverez les informations que vous êtes venu chercher.
